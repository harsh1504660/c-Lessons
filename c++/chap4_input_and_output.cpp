#include<iostream>

using namespace std;

int main()
{
    int num1,num2;
    cout<<"enter the value of num1\n";              // '<<' ths is insertion operator
    cin>>num1;                                          // '>>' this is extraction operator

    cout<<"enter the value of num2\n";
    cin>>num2;

    cout<<"the sum is "<<num1+num2;

    return 0;
}

